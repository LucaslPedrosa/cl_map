using u128 = unsigned __int128;
using u64 = uint64_t;
using u32 = uint32_t;
using u8 = uint8_t;
using uchar = unsigned char;


inline constexpr std::array<u64, 8> secrets{
    0x2d358dccaa6c78a5ull, 0x8bb84b93962eacc9ull, 0x4b33a62ed433d4a3ull, 0x4d5a2da51de1aa47ull,
    0xa0761d6478bd642full, 0xe7037ed1a0b428dbull, 0x90ed1765281c388cull, 0xaaaaaaaaaaaaaaaaull};

inline constexpr u64 seed = 0x123456789ABCDEFULL;


// Collateral: changes secret0 and secret1
inline void generate_secret()
{
}

// =================== READ FUNCTIONS
template <size_t T>
inline void _read(u64 &v, const uchar *p) noexcept
{
  v = 0;
  memcpy(&v, p, T);
}


// =========== END OF READ FUNCTIONS


inline void _mum(u64 &a, u64 &b)
{
  u128 r = static_cast<u128>(a) * b;
  a = static_cast<u64>(r);
  b = static_cast<u64>(r >> 64);
}


inline u64 _mix(u64 a, u64 b)
{
  u128 r = static_cast<u128>(a) * b;
  return static_cast<u64>(r ^ (r >> 64));
}

inline u64 _finalize(u64 a, u64 b, u64 h, size_t len)
{
  a ^= secrets[1];
  b ^= h ^ static_cast<u64>(len);

  _mum(a, b);

  return _mix(a ^ secrets[7], b ^ secrets[3] ^ static_cast<u64>(len));
}


inline u64 rapidhash_(std::string_view s)
{
  const size_t size = s.size();
  size_t used_size = 0;
  const uchar *byte = reinterpret_cast<const uchar *>(s.data());
  const uchar *base = byte;

  u64 hash_ = seed;
  hash_ ^= _mix(secrets[2] ^ seed, secrets[1]);

  u64 a = 0;
  u64 b = 0;


  [[likely]]
  if (size <= 16)
  {
    if (size >= 8)
    {
      _read<8>(a, byte);
      _read<8>(b, byte + size - 8);
    }
    else if (size >= 4)
    {
      _read<4>(a, byte);
      _read<4>(b, byte + size - 4);
    }
    else if (size > 0)
    {
      a = (u64(byte[0]) << 45) | u64(byte[size - 1]) | u64(byte[size >> 1]) << 23;
      b = u64(size);
    }
    return _finalize(a, b, hash_, size);
  }

  while (used_size + 16 < size)
  {
    // READ FUNCTION ALWAYS ZEROES A AND B
    _read<8>(a, byte);
    _read<8>(b, byte + 8);

    hash_ = _mix(hash_ ^ a ^ secrets[0], b ^ secrets[1]);

    byte += 16;
    used_size += 16;
  }

  a = 0;
  b = 0;

  _read<8>(a, base + size - 16);
  _read<8>(b, base + size - 8);

  return _finalize(a, b, hash_, size);
}

