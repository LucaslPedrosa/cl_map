using u128 = unsigned __int128;
using u64 = uint64_t;
using u32 = uint32_t;
using u8 = uint8_t;


inline constexpr std::array<u64, 3> secrets{0xa0761d6478bd642full, 0xe7037ed1a0b428dbull,
                                            0x8ebc6af09c88c6e3ull};

inline constexpr u64 seed = 0x123456789ABCDEFULL;


// Collateral: changes secret0 and secret1
inline void generate_secret()
{
}

// =================== READ FUNCTIONS
template <size_t T>
inline void _read(u64 &v, const char *p)
{
  static_assert(T >= 1 && T <= 8);
  v = 0;
  memcpy(&v, p, T);
}

inline u64 _wyr3(const char *p, u64 len)
{
  return (u64(static_cast<u8>(p[0])) << 16) | (u64(static_cast<u8>(p[len >> 1])) << 8) |
         u64(static_cast<u8>(p[len - 1]));
}

// =========== END OF READ FUNCTIONS


inline u64 _wymum(u64 a, u64 b)
{
  u128 r = static_cast<u128>(a) * b;
  return static_cast<u64>(r ^ (r >> 64));
}

inline u64 wyhash_(std::string_view s)
{
  const u64 size = s.size();
  u64 used_size = 0;
  const char *byte = s.data();

  u64 hash_ = secrets[0] ^ seed;

  u64 a = 0;
  u64 b = 0;

  while (used_size + 16 <= size)
  {
    // READ ALREADYS A = B = 0
    _read<8>(a, byte);
    _read<8>(b, byte + 8);

    hash_ = _wymum(hash_ ^ a ^ secrets[0], b ^ secrets[1]);

    byte += 16;
    used_size += 16;
  }


  u64 remaining = size - used_size;
  a = 0;
  b = 0;

  if (remaining)
  {

    if (remaining > 8)
    {
      _read<8>(a, byte);
      _read<8>(b, byte + remaining - 8);
    }
    else if (remaining >= 4)
    {
      _read<4>(a, byte);
      _read<4>(b, byte + remaining - 4);
    }
    else
    {
      a = _wyr3(byte, remaining);
      b = 0;
    }
    hash_ = _wymum(hash_ ^ a ^ secrets[0], b ^ secrets[1]);
  }
  return _wymum(hash_ ^ size ^ secrets[1], secrets[2]);
}

